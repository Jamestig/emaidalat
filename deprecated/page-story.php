					<p><i>“To be a homestay, ours has first to be a home.”</i></p>
					<p>It is evident that, at least in the growing city centre, Dalat is no longer the simple paradise it used to be.</p>
					<p>For those of us who have known Dalat from just 10, 20, 30 years ago, or wistfully browsed old photobooks of what we now endearingly term “Old Dalat,” the culture and beauty that once pervaded every nook and cranny of Dalat is now being increasingly pushed out and diluted by breakneck industrialisation driven by a hasty and opportunistic tourism industry, importing with it the industrial urbanisation and commercial trends from big cities such as Ha Noi and Saigon. For this reason, Dalat has also long suffered from a lack of culture, good food, and true spaces that reflected the beauty of its surrounding nature, of its idyllic weather and its once slower, more peaceful way of life.</p>
					<p>What could we do in the face of this sadness?</p>
					<p>Our family realised that, rather than stand by and mourn how beautiful Dalat could have been, we must do all that we can with what power we have: to create a small and homely little corner of our own. We wanted to create a space of exquisite food and warm comforts, of beautiful music and natural beauty, one with which to tell you: this is how beautiful Dalat is. All of our love for Dalat lives here.</p>
					<p>Then, when we met our now star chef Hao and recognised her rare and exceptional talent, there was no longer a doubt in our minds that we are to create this space through which she, too, was to shine. It was thus that, in July 2017, émai was beginning to be born.</p>
					<p>With initially only 4 members in our team, we leased an old, wooden house, with creaky floorboards and overgrown shrubs, and put all our dear savings into a simple idea: to once again bring out the inherent beauty of Dalat – and the beauty of life itself.</p>
					<p>Rather than carefully designing the house according to a particular style of interior design, we chose to let organically grow this beautiful 20-year-old wooden Dalat house. Standing inside and breathing in its mountain air, we picked the natural fabrics, lighting, picture frames, and ceramic and furniture pieces that would bring out the beauty of the house and allow it to be simply itself, as it naturally is.</p>
					<p>It was in this true space that all the magic emerged, bit by bit. Putting in everything that we were and had, émai grew as a gorgeous coalescence of all our greatest passions for life, beauty, and nature. Today, each member of the émai family still finds the greatest joy in planting roses, curating music playlists, brewing coffee, and homecooking exquisite Italian dishes that enchant the senses and warms the heart from the inside out. By creating a space in which we can be ourselves and love life to the full, we hope that it can be the same for you, too.</p>
					<p>Today, we are still growing with émai everyday, getting ever better at offering you the most heavenly space in which to enjoy the most precious simple joys of life. Émai’s greatest raison d’être, or reason for being, is to bring pure beauty into your life, and by doing so, to create a space in which the true beauty of Dalat may radiate and blossom in the hearts of Vietnamese and international visitors alike.</p>
					<p>We hope you’ll find deep contentment in our personal selection of jazz and classical music, garden flowers and candles, all handpicked to give you a poetic, authentic atmosphere of warmth, togetherness, and gentle beauty. Let émai be yours and your loved ones’ sanctuary from the brisk Dalat weather -- a true home away from home.</p>
